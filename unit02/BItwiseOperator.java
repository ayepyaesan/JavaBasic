package unit02;

public class BItwiseOperator {

	public static void main(String[] args) {
		int num1=10, num2=5;
		System.out.println("The result after performing num1&num2 " + (num1& num2));
		System.out.println("The result after performing num1|num2 " + (num1| num2));
		System.out.println("The result after performing num1^num2 " + (num1^ num2));
		System.out.println("The result after performing ~num2 " + (~num2));
	}

}
